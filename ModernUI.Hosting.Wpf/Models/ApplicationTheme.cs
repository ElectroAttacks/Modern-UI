namespace ModernUI.Hosting.Wpf.Models;

/// <summary>
///     Represents the theme of the application.
/// </summary>
public enum ApplicationTheme
{

    /// <summary>
    ///     A theme that uses light backgrounds and dark text.
    /// </summary>
    Light,

    /// <summary>
    ///     A theme that uses dark backgrounds and light text.
    /// </summary>
    Dark,

    /// <summary>
    ///     The theme that is set by the system.
    /// </summary>
    SystemDefault
}
